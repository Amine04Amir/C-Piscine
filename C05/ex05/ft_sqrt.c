int	ft_sqrt(int nb)
{
	long	index;

	if (nb <= 0)
	{
		return (0);
	}
	if (nb == 1)
		return (1);
	index = 1;
	while (index * index <= nb)
	{
		if (index * index == nb)
		{
			return (index);
		}
			index++;
	}
	return (0);
}
